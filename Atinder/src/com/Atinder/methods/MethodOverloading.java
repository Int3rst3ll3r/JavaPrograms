package com.Atinder.methods;

public class MethodOverloading {
      
	           int add(int a ,int y) {
	        	    return a+y;
	           }
	           int add(int a ,int y,int x) {
	        	    return a+y;
	           }
	           float add(int a ,float y) {
	        	    return a+y;
	           }
	            double add(double x,int y,int z) {
	            	return x+y+z;
	            }
}
